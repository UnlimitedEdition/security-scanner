def generate_pdf(scan_row: Dict[str, Any]) -> bytes:
    """
    Build a PDF report for one completed scan.

    scan_row is the full DB row from the scans table (or an equivalent
    in-memory dict with the same keys). The scan result payload lives
    under scan_row['result'] as produced by scanner.py.

    Returns the PDF content as bytes, ready to be sent in an
    HTTP response.
    """
    result = scan_row.get("result") or {}
    # scan_row itself carries url/domain at the top level; the nested
    # result may also carry them (scanner.py writes both). Prefer the
    # row-level values because they are what the user originally requested.
    url = scan_row.get("url") or result.get("url") or "unknown"
    domain = scan_row.get("domain") or result.get("domain") or "unknown"

    # Timestamp — prefer completed_at, fall back to created_at, else now
    ts = scan_row.get("completed_at") or scan_row.get("created_at")
    if ts:
        try:
            ts_dt = datetime.fromisoformat(str(ts).replace("Z", "+00:00"))
            scan_date = ts_dt.strftime("%Y-%m-%d %H:%M UTC")
        except (ValueError, TypeError):
            scan_date = str(ts)
    else:
        scan_date = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")

    pdf = _ScanReport(domain=domain, scan_date=scan_date)
    pdf.alias_nb_pages()  # enables {nb} placeholder in footer
    pdf.add_page()

    _draw_cover(pdf, {
        "domain": domain,
        "url": url,
        "scan_time": result.get("scan_time"),
    })
    _draw_score_card(pdf, result.get("score") or {})
    _draw_category_summary(pdf, result.get("results") or [])
    _draw_findings(pdf, result.get("results") or [])

    # fpdf2 returns bytes in recent versions; in older it returns str.
    # Coerce to bytes uniformly.
    output = pdf.output()
    if isinstance(output, (bytes, bytearray)):
        return bytes(output)
    return output.encode("latin-1")
