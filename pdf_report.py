def _draw_cover(pdf: _ScanReport, scan: Dict[str, Any]) -> None:
    """Draw the big branded cover block on page 1."""
    pdf.set_y(25)
    # Logo + brand
    pdf.set_font("Helvetica", "B", 22)
    pdf.set_text_color(*COLOR_ACCENT)
    pdf.cell(0, 10, _ascii("Web Security Scanner"), 0, 1, "L")
    pdf.set_font("Helvetica", "", 11)
    pdf.set_text_color(*COLOR_MUTED)
    pdf.cell(0, 6, _ascii("Passive website security analysis"), 0, 1, "L")
    pdf.ln(8)

    # Title
    pdf.set_font("Helvetica", "B", 18)
    pdf.set_text_color(*COLOR_TEXT)
    pdf.cell(0, 10, _ascii("Security Report"), 0, 1, "L")
    pdf.ln(3)

    # Metadata block
    pdf.set_font("Helvetica", "B", 10)
    pdf.set_text_color(*COLOR_MUTED)
    pdf.cell(40, 7, _ascii("Domain:"), 0, 0)
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(*COLOR_TEXT)
    pdf.cell(0, 7, _ascii(scan.get("domain") or scan.get("url") or "unknown"), 0, 1)

    pdf.set_font("Helvetica", "B", 10)
    pdf.set_text_color(*COLOR_MUTED)
    pdf.cell(40, 7, _ascii("Scanned URL:"), 0, 0)
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(*COLOR_TEXT)
    pdf.cell(0, 7, _ascii(scan.get("url") or "unknown", 90), 0, 1)

    pdf.set_font("Helvetica", "B", 10)
    pdf.set_text_color(*COLOR_MUTED)
    pdf.cell(40, 7, _ascii("Report date:"), 0, 0)
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(*COLOR_TEXT)
    pdf.cell(0, 7, _ascii(pdf.scan_date), 0, 1)

    scan_time = scan.get("scan_time")
    if scan_time:
        pdf.set_font("Helvetica", "B", 10)
        pdf.set_text_color(*COLOR_MUTED)
        pdf.cell(40, 7, _ascii("Scan duration:"), 0, 0)
        pdf.set_font("Helvetica", "", 10)
        pdf.set_text_color(*COLOR_TEXT)
        pdf.cell(0, 7, _ascii(f"{float(scan_time):.2f}s"), 0, 1)

    pdf.ln(10)


def _draw_score_card(pdf: _ScanReport, score_data: Dict[str, Any]) -> None:
    """Big grade + score card near the top of page 1."""
    if not score_data:
        return

    grade = score_data.get("grade", "?")
    score = score_data.get("score", 0)
    label = score_data.get("grade_label", "")

    # Background card
    card_y = pdf.get_y()
    pdf.set_fill_color(245, 245, 250)
    pdf.set_draw_color(*COLOR_BORDER)
    pdf.set_line_width(0.4)
    pdf.rect(MARGIN_L, card_y, CONTENT_W, 45, style="DF")

    # Grade letter (huge)
    pdf.set_xy(MARGIN_L + 10, card_y + 6)
    pdf.set_font("Helvetica", "B", 42)
    grade_color = _grade_color(grade)
    pdf.set_text_color(*grade_color)
    pdf.cell(30, 30, _ascii(grade), 0, 0, "C")

    # Score + label to the right of the grade
    pdf.set_xy(MARGIN_L + 50, card_y + 10)
    pdf.set_font("Helvetica", "B", 22)
    pdf.set_text_color(60, 60, 60)
    pdf.cell(0, 10, _ascii(f"Score: {score}/100"), 0, 1)

    pdf.set_x(MARGIN_L + 50)
    pdf.set_font("Helvetica", "", 11)
    pdf.set_text_color(100, 100, 100)
    pdf.cell(0, 6, _ascii(label), 0, 1)

    # Counts row
    counts = score_data.get("counts") or {}
    pdf.set_xy(MARGIN_L + 50, card_y + 28)
    pdf.set_font("Helvetica", "", 9)
    counts_line = (
        f"CRITICAL: {counts.get('critical', 0)}   "
        f"HIGH: {counts.get('high', 0)}   "
        f"MEDIUM: {counts.get('medium', 0)}   "
        f"LOW: {counts.get('low', 0)}"
    )
    pdf.set_text_color(90, 90, 90)
    pdf.cell(0, 5, _ascii(counts_line), 0, 1)

    pdf.set_y(card_y + 50)
    pdf.set_text_color(*COLOR_TEXT)


def _grade_color(grade: str) -> tuple:
    return {
        "A": (34, 197, 94),
        "B": (132, 204, 22),
        "C": (234, 179, 8),
        "D": (249, 115, 22),
        "F": (239, 68, 68),
    }.get(grade.upper(), (100, 100, 100))


def _draw_category_summary(pdf: _ScanReport, results: List[Dict[str, Any]]) -> None:
    """One-line-per-category pass/fail breakdown below the score card."""
    if not results:
        return
    by_category: Dict[str, Dict[str, int]] = {}
    for r in results:
        cat = r.get("category") or "Other"
        entry = by_category.setdefault(cat, {"passed": 0, "failed": 0})
        if r.get("passed"):
            entry["passed"] += 1
        else:
            entry["failed"] += 1

    pdf.set_font("Helvetica", "B", 12)
    pdf.set_text_color(60, 60, 60)
    pdf.cell(0, 8, _ascii("Category summary"), 0, 1)
    pdf.ln(1)

    pdf.set_font("Helvetica", "", 10)
    for cat in sorted(by_category.keys()):
        entry = by_category[cat]
        line = f"{cat}: {entry['passed']} passed, {entry['failed']} failed"
        pdf.set_text_color(80, 80, 80)
        pdf.cell(5, 6, "-", 0, 0)
        pdf.cell(0, 6, _ascii(line), 0, 1)
    pdf.ln(4)


def _draw_findings(pdf: _ScanReport, results: List[Dict[str, Any]]) -> None:
    """Detailed findings list on subsequent pages."""
    if not results:
        return

    pdf.add_page()
    pdf.set_font("Helvetica", "B", 16)
    pdf.set_text_color(*COLOR_ACCENT)
    pdf.cell(0, 10, _ascii("Detailed findings"), 0, 1)
    pdf.ln(2)

    # Group by category, sort within category by severity then failed-first
    by_category: Dict[str, List[Dict[str, Any]]] = {}
    for r in results:
        by_category.setdefault(r.get("category") or "Other", []).append(r)

    sev_rank = {s: i for i, s in enumerate(SEVERITY_ORDER)}
    for cat in sorted(by_category.keys()):
        items = sorted(
            by_category[cat],
            key=lambda r: (
                0 if not r.get("passed") else 1,
                sev_rank.get(r.get("severity", "INFO"), 99),
                _ascii(r.get("name", "")),
            ),
        )

        pdf.set_font("Helvetica", "B", 12)
        pdf.set_text_color(40, 40, 40)
        pdf.cell(0, 7, _ascii(cat), 0, 1)
        pdf.set_draw_color(200, 200, 200)
        pdf.set_line_width(0.2)
        pdf.line(MARGIN_L, pdf.get_y(), PAGE_W - MARGIN_R, pdf.get_y())
        pdf.ln(2)

        for r in items:
            _draw_finding_row(pdf, r)
        pdf.ln(2)


def _draw_finding_row(pdf: _ScanReport, r: Dict[str, Any]) -> None:
    """Single finding block: colored severity chip + name + message."""
    passed = r.get("passed", False)
    sev = r.get("severity", "INFO")
    sev_color = SEVERITY_COLOR.get(sev, (120, 120, 120))
    name = _ascii(r.get("name") or r.get("check") or "(unnamed)", 70)
    message = _ascii(r.get("message") or r.get("details") or "", 220)

    # Status + severity chip
    status_text = "PASS" if passed else sev
    status_color = COLOR_GREEN if passed else sev_color

    y0 = pdf.get_y()
    # Left colored chip
    pdf.set_fill_color(*status_color)
    pdf.rect(MARGIN_L, y0 + 1, 3, 4, style="F")

    # Name (bold)
    pdf.set_xy(MARGIN_L + 5, y0)
    pdf.set_font("Helvetica", "B", 9)
    pdf.set_text_color(40, 40, 40)
    pdf.cell(0, 5, name, 0, 1)

    # Status label
    pdf.set_x(MARGIN_L + 5)
    pdf.set_font("Helvetica", "B", 7)
    pdf.set_text_color(*status_color)
    pdf.cell(0, 3, _ascii(f"[{status_text}]"), 0, 1)

    # Message (regular, wrapped)
    if message:
        pdf.set_x(MARGIN_L + 5)
        pdf.set_font("Helvetica", "", 8)
        pdf.set_text_color(90, 90, 90)
        pdf.multi_cell(CONTENT_W - 5, 4, message, 0, "L")

    pdf.ln(1)


