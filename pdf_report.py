"""
PDF report generation for Pro plan.

Given a completed scan row (the JSONB blob from the scans table), this
module produces a branded multi-page PDF report. Used by the
GET /api/scan/{id}/pdf endpoint, which is gated on an active Pro
subscription.

Design notes:

- fpdf2 is chosen over weasyprint because weasyprint needs system libs
  (pango, cairo) that are not in the python:3.11-slim base image. fpdf2
  is pure Python and works with no extra install.

- fpdf2's built-in Helvetica font is latin1 only. Scanner check messages
  use Serbian Latin with diacritics (cscdz) which latin1 does not cover.
  We transliterate to diacritic-free ASCII before every draw call via
  _ascii(). The text stays readable ("izostavljanje" instead of
  "izostavljanje") and no TTF file has to be bundled.

- Every public helper returns a PDF as bytes, so the api.py endpoint
  can wrap it in a FileResponse or StreamingResponse without touching
  disk.
"""
from __future__ import annotations

import unicodedata
from datetime import datetime
from typing import Any, Dict, List, Optional

from fpdf import FPDF

# Page layout
PAGE_W = 210  # A4 width in mm
MARGIN_L = 15
MARGIN_R = 15
MARGIN_T = 20
CONTENT_W = PAGE_W - MARGIN_L - MARGIN_R  # 180mm

# Brand colors (RGB tuples) — matched to the scanner's web UI
COLOR_BG = (10, 12, 21)
COLOR_ACCENT = (167, 139, 250)     # #a78bfa purple
COLOR_TEXT = (232, 234, 246)
COLOR_MUTED = (123, 131, 166)
COLOR_BORDER = (37, 42, 74)
COLOR_GREEN = (74, 222, 128)
COLOR_YELLOW = (234, 179, 8)
COLOR_ORANGE = (249, 115, 22)
COLOR_RED = (239, 68, 68)

SEVERITY_COLOR = {
    "CRITICAL": COLOR_RED,
    "HIGH": COLOR_ORANGE,
    "MEDIUM": COLOR_YELLOW,
    "LOW": (132, 204, 22),
    "INFO": (156, 163, 175),
}

SEVERITY_ORDER = ["CRITICAL", "HIGH", "MEDIUM", "LOW", "INFO"]


def _ascii(text: Any, max_len: Optional[int] = None) -> str:
    """
    Transliterate a string to ASCII so fpdf2's latin1 Helvetica can render it.

    Drops combining marks (diacritics): kosmicni -> kosmicni.
    Any still-unsupported character (emoji, Cyrillic) becomes '?'.
    If max_len is given, the result is truncated with an ellipsis.
    """
    if text is None:
        return ""
    s = str(text)
    # NFKD separates base chars from combining marks, then we filter the marks
    decomposed = unicodedata.normalize("NFKD", s)
    ascii_str = "".join(c for c in decomposed if not unicodedata.combining(c))
    # Replace anything still outside latin1 with '?'
    ascii_str = ascii_str.encode("latin-1", errors="replace").decode("latin-1")
    if max_len and len(ascii_str) > max_len:
        ascii_str = ascii_str[: max_len - 1] + "~"
    return ascii_str


class _ScanReport(FPDF):
    """FPDF subclass with auto-header on every new page and auto-footer."""

    def __init__(self, domain: str, scan_date: str):
        super().__init__(orientation="P", unit="mm", format="A4")
        self.domain = _ascii(domain, 60)
        self.scan_date = _ascii(scan_date, 40)
        self.set_auto_page_break(auto=True, margin=20)
        self.set_margins(MARGIN_L, MARGIN_T, MARGIN_R)
        self.set_creator("Web Security Scanner Pro")
        self.set_author("Web Security Scanner")
        self.set_title(f"Security Report for {self.domain}")

    def header(self):
        # Thin top bar with brand + domain on every page except the first
        # (the first page has its own hero block). Page 1 is drawn before
        # header() is called again, so the check compares page_no() > 1.
        if self.page_no() == 1:
            return
        self.set_y(8)
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*COLOR_ACCENT)
        self.cell(0, 5, _ascii("Web Security Scanner"), 0, 0, "L")
        self.set_font("Helvetica", "", 8)
        self.set_text_color(*COLOR_MUTED)
        self.cell(0, 5, self.domain, 0, 1, "R")
        # Thin accent rule
        self.set_draw_color(*COLOR_ACCENT)
        self.set_line_width(0.3)
        self.line(MARGIN_L, 16, PAGE_W - MARGIN_R, 16)
        self.ln(6)

    def footer(self):
        self.set_y(-15)
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*COLOR_MUTED)
        left = _ascii("Generated by Web Security Scanner Pro")
        right = f"Page {self.page_no()}/{{nb}}"
        self.cell(0, 4, left, 0, 0, "L")
        self.cell(0, 4, right, 0, 1, "R")
        self.set_font("Helvetica", "", 6)
        self.cell(0, 3, _ascii(f"Report generated {self.scan_date}"), 0, 0, "L")
        self.cell(0, 3, _ascii("security-scanner-ruddy.vercel.app"), 0, 1, "R")


